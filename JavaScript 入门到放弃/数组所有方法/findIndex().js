/*
Array.prototype.findIndex()

说明：
  该findIndex()方法返回数组中满足提供的测试函数的第一个元素的索引。否则它返回-1，表示没有元素通过测试。 


参数:
  callbackFn
    对数组中的每个值执行的函数true，直到函数返回 ，表明找到了满意的元素。

    它需要三个参数：

    element
      数组中正在处理的当前元素。

    index 可选的
      数组中正在处理的当前元素的索引。

    array 可选的
      该数组findIndex()被调用。

  thisArg 可选的
    this执行时 使用的可选对象callbackFn。


返回值:
通过测试的数组中第一个元素的索引。否则返回-1。
*/
