<template>
  <div>
    <h1>当前求和为:{{ sum }}</h1>
    <select v-model="n">
      <option :value="1">1</option>
      <option :value="2">2</option>
      <option :value="3">3</option>
    </select>
    <button @click="increment">+</button>
    <button @click="decrement">-</button>
    <button @click="incrementOdd">当前求和为奇数再加</button>
    <button @click="incrementWait">等一等再加</button>
  </div>
</template>

<script>
export default {
  name: "Count",
  data() {
    return {
      sum: 0,
      n: 1,
    };
  },
  methods: {
    // 加法
    increment() {
      this.sum += this.n;
    },
    // 减法
    decrement() {
      this.sum -= this.n;
    },
    // 求和为奇数再加
    incrementOdd() {
      if (this.sum % 2) {
        this.sum += this.n;
      }
    },
    // 等一等再加
    incrementWait() {
      setTimeout(() => {
        this.sum += this.n;
      }, 500);
    },
  },
};
</script>

<style scoped>
button {
  margin-left: 5px;
}
</style>
